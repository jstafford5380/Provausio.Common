namespace Provausio.Common
{
    /// <summary>
    /// Values used to indicate the attitude of survey-reviews
    /// </summary>
    public enum ReviewSentiment
    {
        /// <summary>
        /// Indicates undetermined survey-review sentiment
        /// </summary>
        Unspecified = 0,

        /// <summary>
        /// Indicates positive survey-review sentiment
        /// </summary>
        Positive = 1,

        /// <summary>
        /// Indicates negative survey-review sentiment
        /// </summary>
        Negative = 2
    }
}
